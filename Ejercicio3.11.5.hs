-- ¿Por qué no es factible en general que los tipos de función sean instancias de la clase Eq?
-- ¿Cuando es factible?

-- Los tipos de función no pueden ser instancias de la clase Eq en general debido a la naturaleza de las funciones y la forma
-- en la que se comparan.
-- En casos especificos como funciones con un dominio finito es posible implementar la clase Eq.
